package com.engineringdigest.javaInOneVideo.PatternP;

public class square {
    public static void main(String[] args) {

        int n=4;



        for (int i = 0; i < n; i++) {
            int  count =1;
            for (int j = 0; j < n; j++) {
                System.out.print(count );
                count++;

            }
            System.out.println();

        }
    }
}
